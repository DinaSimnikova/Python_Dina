# Создать список, состоящий из кубов нечётных чисел от 1 до 1000:
# a. Вычислить сумму тех чисел из этого списка,
# сумма цифр которых делится нацело на 7.
# Например, число «19 ^ 3 = 6859» # будем включать в сумму,
# так как 6 + 8 + 5 + 9 = 28 – делится нацело на 7.
# Внимание: использовать только арифметические операции!
#
# b. К каждому элементу списка добавить 17 и заново вычислить
# сумму тех чисел из этого списка, сумма цифр которых делится нацело на 7.
#
# c. Решить задачу под пунктом b, не создавая новый список.

arr = [i**3 for i in range(1, 1001, 2)]
print(arr)

#a
def sum_not7(item):
    per_sum = 0 # сумма цифр каждого числа
    while item > 0:
        new_item = item % 10
        per_sum = per_sum + new_item
        item = item // 10
    return per_sum

arr = [i**3 for i in range(1, 1001, 2)]

final_sum1 = sum(filter(lambda num: sum_not7(num) % 7 == 0, arr))
print(final_sum1)

#b&c
final_sum2 = sum(filter(lambda num: sum_not7(num + 17) % 7 == 0, arr))
print(final_sum2)

