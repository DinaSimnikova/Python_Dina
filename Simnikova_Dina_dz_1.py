# Task1
# Реализовать вывод информации о промежутке времени в зависимости
# от его продолжительности duration в секундах:
# до минуты: <s> сек;
# до часа: <m> мин <s> сек;
# до суток: <h> час <m> мин <s> сек;
# * в остальных случаях: <d> дн <h> час <m> мин <s> сек.

#1 min = 60 sec
#1 hour = 60 * 60 = 3600 sec
#1 day = 60 * 60 * 24 = 86400 sec
#1 week = 60 * 60 * 24 * 7 = 604800 sec

duration = int(input('Введите время в секундах: '))
if duration <= 60:
   print(duration, 'sec')
else:
    week = duration // 604800
    day = (duration - (week * 604800)) // 86400
    hour = (duration - ((week * 604800) + (day * 86400))) // 3600
    minutes = (duration - ((week * 604800) + (day * 86400) + (hour * 3600))) // 60
    seconds = duration - ((week * 604800) + (day * 86400) + (hour * 3600) + (minutes * 60))
    print(week, 'week', day, 'days', hour, 'hours', minutes, 'minutes', seconds, 'seconds')

